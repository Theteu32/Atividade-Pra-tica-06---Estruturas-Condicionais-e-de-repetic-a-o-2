let dia = prompt("Digite um numero de 1 a 7")

if (dia <=0 || dia >= 8){
    document.write("Digite um numero correto!!")
}
else if (dia == 1){
    document.write("Hoje e domingo!!")
}
else if (dia == 2){
    document.write("Hoje e segunda!!")
}
else if (dia == 3){
    document.write("Hoje e terça!!")
}
else if (dia == 4){
    document.write("Hoje e quarta!!")
}
else if (dia == 5){
    document.write("Hoje e quinta!!")
}
else if (dia == 6){
    document.write("Hoje e sexta!!")
}
else{
    document.write("Hoje e sábado!!")
}
